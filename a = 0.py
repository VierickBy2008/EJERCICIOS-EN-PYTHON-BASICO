a = 1
suma_pares = 0
suma_impares = 0

while a <= 20:
    if a % 2 == 0:
        suma_pares += a
    else:
        suma_impares += a

    a += 1

print("la suma de los numeros pares hasta el 20 es:", suma_pares)
print("la suma de los numeros impares hasta el 20 es:", suma_impares)


